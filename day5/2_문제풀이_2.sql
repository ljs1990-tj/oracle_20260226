-- 5. 각 부서별 평균 금여를 구한 후 평균 급여를 기준으로 내림차순 하시오. ( 10점 )
-- 사용테이블 : TBL_EMP, TBL_DEPT
-- 출력 컬럼 : 부서 이름, 평균 급여
SELECT DEPT_NAME, AVG(SALARY) SAL
FROM TBL_EMP E
INNER JOIN TBL_DEPT D ON E.DEPT_ID = D.DEPT_ID
GROUP BY DEPT_NAME
ORDER BY SAL DESC;

-- 6. 모든 부서장들의 급여 평균보다 높은 급여를 받는 직원들을 출력하시오. ( 10점 )
--   : TBL_EMP, TBL_DEPT
-- 출력 컬럼 : 직원 이름, 급여, 부서장들의 평균 급여

SELECT 
    EMP_NAME, SALARY, AVG_SAL
FROM TBL_EMP E
INNER JOIN (
    SELECT AVG(SALARY) AS AVG_SAL
    FROM TBL_EMP E
    INNER JOIN TBL_DEPT D ON E.EMP_ID = D.HEAD_ID
) T ON 1=1
WHERE SALARY > AVG_SAL;

SELECT 
    EMP_NAME, SALARY, ROUND(AVG_SAL, 1)
FROM TBL_EMP E
INNER JOIN (
    SELECT AVG(SALARY) AS AVG_SAL
    FROM TBL_EMP E
    INNER JOIN TBL_DEPT D ON E.EMP_ID = D.HEAD_ID
) T ON SALARY > AVG_SAL;

SELECT 
    EMP_NAME, 
    SALARY,
    (
        SELECT AVG(SALARY) AS AVG_SAL
        FROM TBL_EMP E
        INNER JOIN TBL_DEPT D ON E.EMP_ID = D.HEAD_ID
    )
FROM TBL_EMP 
WHERE SALARY > (
    SELECT AVG(SALARY) AS AVG_SAL
    FROM TBL_EMP E
    INNER JOIN TBL_DEPT D ON E.EMP_ID = D.HEAD_ID
);

