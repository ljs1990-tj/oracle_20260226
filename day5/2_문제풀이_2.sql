FROM TBL_EMP E
WHERE SALARY = (
    SELECT MAX(SALARY)
    FROM TBL_PROJECT P
    INNER JOIN TBL_ASSIGNMENT A ON P.PROJ_ID = A.PROJ_ID
    INNER JOIN TBL_EMP E ON E.EMP_ID = A.EMP_ID
    WHERE PROJ_NAME = '모바일 앱 개발'
);

